    <h2>¿Cómo funcionan los planes?</h2>
    
    <p>Los planes se basan en el espacio de almacenamiento que necesitas para documentación
    clínica, el número de organizaciones que generarán y consumirán datos, y la cantidad de
    distintos tipos de documentos clínicos (plantillas) que usará cada organización.</p>
    
    <p>La subscripción a un plan se paga de forma anual, y el crédito es asociado a tu cuenta.
    Cada mes se ejecutará un débito por el costo mensual del plan, el cual se reflejará en tu
    balance.</p>
    
    <p>Puedes solicitar cambiar de plan en cualquier momento, y el crédito actual de tu cuenta
    será prorrateado al precio del nuevo plan. Por ejemplo si has contratado un plan Básico, y
    luego de dos meses necesitas crecer, en tu balance tendrás un crédito de 70 USD (7 * 12 - 7 * 2),
    si el nuevo plan es Estándar, tu crédito cubrirá unos 5 meses de suscripción al nuevo plan.</p>
    
