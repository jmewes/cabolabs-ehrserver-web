    <h2>Use the EHRServer to gather clinical data</h2>
    <p>
      The EHRServer can be used to store any kind of clinical structure without changing source code or the database schema. You
      don't need to change your systems to do so, just build a small client program to copy your data into the EHRServer using the
      standard openEHR format. This can be done from many data sources, integrating all the data you need into one place. Then by
      creating data queries, using the visual EHRServer Query Builder, will allow you to retrieve your data sets based on certain
      conditions. Then that data can be used to create reports, calculate variables, indicators and do any kind of data analysis.
    </p>
    <p>
      The openEHR clinical content management methodology is key to have consistent data to analyze in an efficient way, that means
      without weeks of data analysis to make sense of the data, start cleaning and organizing (preprocessing) before doing anything
      valuable with the data. With this methodlogy and the EHRServer, the preparation stage is minimal, allowing to put your effort
      on your research or educational goal.
    </p>
